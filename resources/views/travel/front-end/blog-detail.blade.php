                                <p style="text-align: justify;">Ngày 15/11/2015 vừa qua, đường bay mới Hải Phòng - Nha Trang (tỉnh Khánh Hòa) đã chính thức đi vào hoạt động, đánh dấu sự kết nối mang ý nghĩa quan trọng giữa hai thành phố trọng điểm du lịch quốc gia, góp phần thúc đẩy phát triển du lịch cả nước và phát triển kinh tế xã hội hai địa phương.</p>

                                <p style="text-align: center;"><img src="{{ asset('vendor/image/1-nhatrang.jpg') }}" />
                                </p>

                                <p style="text-align: justify;">Đường bay Hải Phòng - Nha Trang được Vietnam Airlines khai thác với tần suất 4 chuyến/tuần vào các ngày thứ hai, tư, sáu và chủ nhật, bằng máy bay Airbus A321. Các chuyến bay xuất phát từ Nha Trang lúc 8 giờ 20 và từ Hải Phòng lúc 10 giờ 50, thời gian bay 1 giờ 45 phút. Giá vé máy bay một chiều từ 799 nghìn đồng/vé cho hạng phổ thông và 3 triệu đồng/vé hạng thương gia.</p>

                                <p style="text-align: center;"><img src="{{ asset('vendor/image/2-nhatrang.jpg') }}" />
                                </p>

                                <p style="text-align: justify;">Từ năm 2016, khi hàng loạt dự án giao thông trọng điểm trên địa bàn hoàn thành, dự kiến sẽ mở ra nhiều cơ hội mới, đưa Hải Phòng trở thành điểm giao thương quan trọng
